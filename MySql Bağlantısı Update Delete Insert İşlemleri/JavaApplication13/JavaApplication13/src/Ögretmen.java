    private void KayitEt(String adsoyad, String kulad, String kulsif, String mail, String not) {
        Connection dbconn = VeriBaglanti.baglanti();
        if (dbconn != null) {
            try {
                PreparedStatement st = (PreparedStatement) dbconn.prepareStatement("INSERT INTO ögrenci(adi_soyadi,kullanici_adi,kullanici_sifre,email,karnenotu) VALUES(?,?,?,?,?)");

                st.setString(1, adsoyad);
                st.setString(2, kulad);
                st.setString(3, kulsif);
                st.setString(4, mail);
                st.setString(5, not);

                int res = st.executeUpdate();

                JOptionPane.showMessageDialog(this, "Kullanıcı kaydı yapıldı.", "Başarılı", JOptionPane.INFORMATION_MESSAGE);

                id.setText("");
                adi_soyadi.setText("");
                kullanici_adi.setText("");
                email.setText("");
                kullanici_adi.setText("");
                kullanici_sifre.setText("");
                karnenotu.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(Kayit.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("Bağlantı ulaşılabilir değil !");
        }
    }

    private void Güncelle(String i, String adsoyad, String not, String kulad, String kulsif, String mail) {
        Connection dbconn = VeriBaglanti.baglanti();
        if (dbconn != null) {
            try {
                PreparedStatement st = (PreparedStatement) dbconn.prepareStatement("UPDATE ögrenci set adi_soyadi=?,kullanici_adi=?,kullanici_sifre=?,email=?,karnenotu=? where id=? ");

                st.setString(1, adsoyad);
                st.setString(2, kulad);
                st.setString(3, kulsif);
                st.setString(4, mail);
                st.setString(5, not);
                st.setString(6, i);

                int res = st.executeUpdate();

                JOptionPane.showMessageDialog(this, "Güncelleme yapıldı.", "Başarılı", JOptionPane.INFORMATION_MESSAGE);

                id.setText("");
                adi_soyadi.setText("");
                kullanici_adi.setText("");
                email.setText("");
                kullanici_adi.setText("");
                kullanici_sifre.setText("");
                karnenotu.setText("");

            } catch (SQLException ex) {
                Logger.getLogger(Kayit.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("Bağlantı ulaşılabilir değil !");
        }
    }

