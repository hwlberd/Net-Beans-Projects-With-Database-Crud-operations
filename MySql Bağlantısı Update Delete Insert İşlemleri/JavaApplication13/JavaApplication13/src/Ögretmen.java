    public void tabloGüncelle() {
        Connection dbconn = VeriBaglanti.baglanti();

        int q;
        int i;
        try {
            PreparedStatement st = (PreparedStatement) dbconn.prepareStatement("select * from ögrenci");
            ResultSet res = st.executeQuery();
            ResultSetMetaData StData = (ResultSetMetaData) res.getMetaData();

            q = StData.getColumnCount();

            DefaultTableModel RecordTable = (DefaultTableModel) tablo.getModel();
            RecordTable.setRowCount(0);

            while (res.next()) {

                Vector columnData = new Vector();
                for (i = 1; i < q; i++) {
                    columnData.add(res.getString("id"));
                    columnData.add(res.getString("adi_soyadi"));
                    columnData.add(res.getString("karnenotu"));
                    columnData.add(res.getString("kullanici_adi"));
                    columnData.add(res.getString("kullanici_sifre"));
                    columnData.add(res.getString("email"));

                }
                RecordTable.addRow(columnData);
            }

        } catch (Exception ex) {
            Logger.getLogger(Ögrenci.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

